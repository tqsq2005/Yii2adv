namespace common\populac\behaviors;

use Yii;
use yii\base\Behavior;

/**
 * Status behavior. Adds statuses to models
 * @package common\populac\behaviors
 */
class StatusController extends Behavior
{
    public $model;

    public function changeStatus($id, $status)
    {
        $modelClass = $this->model;

        if(($model = $modelClass::findOne($id))){
            $model->status = $status;
            $model->update();
        }
        else{
            $this->error = 'Not found';
        }

        return $this->owner->formatResponse('Status successfully changed');
    }
}